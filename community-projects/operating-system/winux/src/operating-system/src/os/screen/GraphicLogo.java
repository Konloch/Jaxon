    public static void doIntro(int sleep) {
        int start = 0xC8;
        int end = 0xF7;
        int dist = end - start;

        Graphic g = new Graphic();
        for(int y = 0; y < Graphic.HEIGHT; y++) {
            for(int x = 0; x < Graphic.WIDTH; x++) {
                g.setPtrAndInc((byte) ((y * dist / 200) + start));
            }
        }

        start = 0x38;
        end = 0x67;
        GraphicLogo.slapLogoOnTop(g, sleep, start, end);
    }

